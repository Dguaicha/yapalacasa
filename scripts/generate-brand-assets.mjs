/**
 * Builds splash + app icons from ./logo.jpg with a seamless background
 * sampled from the image corners (matches the logo mat / paper color).
 *
 * Run: node scripts/generate-brand-assets.mjs
 */
import { readFileSync, writeFileSync } from 'fs'
import { dirname, join } from 'path'
import { fileURLToPath } from 'url'
import sharp from 'sharp'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const inputPath = join(root, 'logo.jpg')

function rgbToHex(r, g, b) {
  const h = (n) => n.toString(16).padStart(2, '0')
  return `#${h(r)}${h(g)}${h(b)}`.toUpperCase()
}

async function averageCorner(sharpImg, left, top, w, h) {
  const { data, info } = await sharpImg
    .clone()
    .extract({
      left,
      top,
      width: w,
      height: h
    })
    .ensureAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true })

  const ch = info.channels
  let r = 0
  let g = 0
  let b = 0
  let n = 0
  for (let i = 0; i < data.length; i += ch) {
    r += data[i]
    g += data[i + 1]
    b += data[i + 2]
    n++
  }
  return {
    r: Math.round(r / n),
    g: Math.round(g / n),
    b: Math.round(b / n)
  }
}

async function main() {
  const base = sharp(inputPath)
  const meta = await base.metadata()
  const W = meta.width ?? 1000
  const H = meta.height ?? 1000
  const s = Math.min(48, Math.floor(W / 8), Math.floor(H / 8))

  const corners = await Promise.all([
    averageCorner(base, 0, 0, s, s),
    averageCorner(base, W - s, 0, s, s),
    averageCorner(base, 0, H - s, s, s),
    averageCorner(base, W - s, H - s, s, s)
  ])

  const R = Math.round(corners.reduce((a, c) => a + c.r, 0) / 4)
  const G = Math.round(corners.reduce((a, c) => a + c.g, 0) / 4)
  const B = Math.round(corners.reduce((a, c) => a + c.b, 0) / 4)
  const hex = rgbToHex(R, G, B)

  const logoResized = await sharp(inputPath)
    .resize({
      width: 920,
      height: 920,
      fit: 'inside',
      withoutEnlargement: true
    })
    .toBuffer()

  // Portrait splash (common phone ratio) — full bleed, logo centered
  const splashW = 1242
  const splashH = 2688
  await sharp({
    create: {
      width: splashW,
      height: splashH,
      channels: 3,
      background: { r: R, g: G, b: B }
    }
  })
    .composite([{ input: logoResized, gravity: 'center' }])
    .png()
    .toFile(join(root, 'assets', 'splash-icon.png'))

  // App icon (square) — same background, logo ~62% max width
  const iconSize = 1024
  const logoForIcon = await sharp(inputPath)
    .resize({
      width: Math.round(iconSize * 0.62),
      height: Math.round(iconSize * 0.62),
      fit: 'inside',
      withoutEnlargement: true
    })
    .toBuffer()

  await sharp({
    create: {
      width: iconSize,
      height: iconSize,
      channels: 3,
      background: { r: R, g: G, b: B }
    }
  })
    .composite([{ input: logoForIcon, gravity: 'center' }])
    .png()
    .toFile(join(root, 'assets', 'icon.png'))

  // Android adaptive foreground (safe zone ~66% — logo a bit smaller)
  const adapt = 1024
  const logoAdapt = await sharp(inputPath)
    .resize({
      width: Math.round(adapt * 0.52),
      height: Math.round(adapt * 0.52),
      fit: 'inside',
      withoutEnlargement: true
    })
    .toBuffer()

  await sharp({
    create: {
      width: adapt,
      height: adapt,
      channels: 3,
      background: { r: R, g: G, b: B }
    }
  })
    .composite([{ input: logoAdapt, gravity: 'center' }])
    .png()
    .toFile(join(root, 'assets', 'adaptive-icon.png'))

  // Web favicon
  await sharp(join(root, 'assets', 'icon.png'))
    .resize(48, 48, { fit: 'cover' })
    .png()
    .toFile(join(root, 'assets', 'favicon.png'))

  const brandTs = `// Auto-generated by scripts/generate-brand-assets.mjs — run after changing logo.jpg
export const brandSplashBackground = '${hex}' as const
`
  writeFileSync(join(root, 'src', 'theme', 'brandAssets.ts'), brandTs, 'utf8')

  const appJsonPath = join(root, 'app.json')
  const app = JSON.parse(readFileSync(appJsonPath, 'utf8'))
  app.expo.backgroundColor = hex
  app.expo.splash.backgroundColor = hex
  app.expo.splash.resizeMode = 'contain'
  app.expo.web = app.expo.web || {}
  app.expo.web.backgroundColor = hex
  if (!app.expo.android) app.expo.android = {}
  app.expo.android.adaptiveIcon = {
    foregroundImage: './assets/adaptive-icon.png',
    backgroundColor: hex
  }
  writeFileSync(appJsonPath, JSON.stringify(app, null, 2) + '\n', 'utf8')

  const tailwindPath = join(root, 'tailwind.config.js')
  let tw = readFileSync(tailwindPath, 'utf8')
  tw = tw.replace(/background:\s*'#[0-9A-Fa-f]{6}'/, `background: '${hex}'`)
  writeFileSync(tailwindPath, tw, 'utf8')

  console.log('Brand assets generated.')
  console.log('  Background (seamless):', hex)
  console.log('  assets/splash-icon.png, icon.png, adaptive-icon.png, favicon.png')
  console.log('  src/theme/brandAssets.ts')
  console.log('  app.json + tailwind.config.js (background)')
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
